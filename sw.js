/* Particle Monitor — service worker
   Caches the app shell so the PWA opens instantly (and offline shows the UI).
   API calls to api.particle.io are never cached. */
const CACHE = 'particle-monitor-v1';
const SHELL = ['./', './index.html', './manifest.json', './icon-192.png', './icon-512.png'];

self.addEventListener('install', e => {
  e.waitUntil(caches.open(CACHE).then(c => c.addAll(SHELL)).then(() => self.skipWaiting()));
});

self.addEventListener('activate', e => {
  e.waitUntil(
    caches.keys().then(keys =>
      Promise.all(keys.filter(k => k !== CACHE).map(k => caches.delete(k)))
    ).then(() => self.clients.claim())
  );
});

self.addEventListener('fetch', e => {
  const url = new URL(e.request.url);
  // Never intercept Particle API traffic (live data + SSE stream)
  if (url.hostname === 'api.particle.io') return;
  if (e.request.method !== 'GET') return;
  // Network-first for the shell, falling back to cache when offline
  e.respondWith(
    fetch(e.request)
      .then(resp => {
        const copy = resp.clone();
        caches.open(CACHE).then(c => c.put(e.request, copy));
        return resp;
      })
      .catch(() => caches.match(e.request))
  );
});
